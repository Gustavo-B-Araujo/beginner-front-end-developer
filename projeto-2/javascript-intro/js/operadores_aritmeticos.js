// Operadores aritméticos
console.log(1+1);    /// adição
console.log(1-1);    // subtração
console.log(1*1);    // multiplicação
console.log(1/1);    // divisão
console.log(1%1);    // resto da divisão
console.log(1**1);   // exponenciação

// Incremento e Decremento
let x = 5;
x = x + 1;
console.log(x);

let y = 5
console.log(y++); // atualiza mas não mostra a atualização
console.log(++y); // atualiza e mostra a atualização

y += 10; // outra forma de incremento

